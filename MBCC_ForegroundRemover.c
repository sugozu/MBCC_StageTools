void patch( char * fnme, int offset, int mode ){
	FILE * fstr = fopen( fnme,"rb+" );
	if( fstr==NULL ) printf( "Error: Failed to patch %s.\n", fnme );
	else{
		fseek( fstr, offset, SEEK_SET );
		if( mode )
			switch( offset ){//revert
				case 108: fwrite( bgGrass, sizeof(char),4, fstr ); break;
				case 116: fwrite( bg55, sizeof(char),  16, fstr ); break;
				case 120: fwrite( bg59, sizeof(char), 208, fstr ); break;
			}
		else
			switch( offset ){//patch
				case 108: fwrite( FF, sizeof(char),4, fstr ); break;//4
				case 116: for( int i=0; i< 4; i++ ) fwrite( FF, sizeof(char),  4, fstr ); break;//16
				case 120: for( int i=0; i<52; i++ ) fwrite( FF, sizeof(char),  4, fstr ); break;//208
			}
		fclose( fstr );
		printf( "%s patched.\n", fnme );
	}
}

